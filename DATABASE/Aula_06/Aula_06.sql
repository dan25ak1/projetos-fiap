--10
SELECT FUNC.CD_DEPTO,
       FUNC.NM_FUNCIONARIO,
       FUNC.VL_SALARIO,
       FUNC.CD_DEPTO
FROM T_SIP_FUNCIONARIO FUNC
WHERE FUNC.CD_DEPTO > 1
ORDER BY 4, NM_FUNCIONARIO DESC; -- 4 SENDO "CD_DEPTO" INDICIDE DA COLUNA DO SELECT, 2 SENDO "NM_FUNCIOARIO" INDICIDE DA COLUNA DO SELECT

--11
