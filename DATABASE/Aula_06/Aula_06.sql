       F.NM_FUNCIONARIO,
       F.CD_DEPTO,
       F.DT_ADMISSAO,
       F.VL_SALARIO
FROM T_SIP_FUNCIONARIO F
WHERE TO_CHAR(F.DT_ADMISSAO,'YYYY') IN ('2015', '2018', '2021') -- CONVERTENDO UMA DATA EM UMA STRING

