--16
SELECT F.CD_FUNC,
       F.NM_FUNCIONARIO,
       F.CD_DEPTO,
       F.DT_ADMISSAO
FROM T_SIP_FUNCIONARIO F
WHERE F.DT_ADMISSAO >= TO_DATE('01/01/2012','DD/MM/YYYY')
AND 'TRUE'='TRUE'; --COLOCANDO DOIS VALORES IGUAIS TEXTO OU NUMERO. (2=2)

--17
SELECT F.CD_FUNC,
       F.NM_FUNCIONARIO,
       F.CD_DEPTO,
       F.DT_ADMISSAO
FROM T_SIP_FUNCIONARIO F
WHERE F.DT_ADMISSAO >= TO_DATE('01/01/2018','DD/MM/YYYY')
OR F.CD_DEPTO = 7;

--18
SELECT F.CD_FUNC,
       F.NM_FUNCIONARIO,
       F.CD_DEPTO,
       F.DT_ADMISSAO,
       F.VL_SALARIO
FROM T_SIP_FUNCIONARIO F
WHERE NOT F.CD_DEPTO = 10;

--19
SELECT F.CD_FUNC,
       F.NM_FUNCIONARIO,
       F.CD_DEPTO,
       F.DT_ADMISSAO,
       F.VL_SALARIO
FROM T_SIP_FUNCIONARIO F
WHERE F.DT_ADMISSAO BETWEEN TO_DATE ('01/01/2021','DD/MM//YYYY')
AND TO_DATE('31/12/2021','DD/MM/YYYY');

--20
