       F.NM_FUNCIONARIO,
       F.CD_DEPTO,
       F.DT_ADMISSAO,
       F.VL_SALARIO
FROM T_SIP_FUNCIONARIO F
WHERE F.CD_DEPTO IN (1,7,4);

--21
