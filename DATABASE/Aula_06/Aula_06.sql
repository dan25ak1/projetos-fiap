       F.NM_FUNCIONARIO NOME_FUNCIONARIO,
       F.VL_SALARIO "SALÁRIO BRUTO",
       F.CD_DEPTO CODIGO_DEPARTAMENTO
FROM T_SIP_FUNCIONARIO F

--12 OP: >,<, <=, >=, AND, OR, NOT, IN
SELECT F.CD_FUNC MATRICULA,
       F.NM_FUNCIONARIO NOME_FUNCIONARIO,
       F.VL_SALARIO "SALÁRIO BRUTO",
       F.CD_DEPTO CODIGO_DEPARTAMENTO
FROM T_SIP_FUNCIONARIO F
WHERE VL_SALARIO > 2999.99
ORDER BY F.VL_SALARIO DESC;

